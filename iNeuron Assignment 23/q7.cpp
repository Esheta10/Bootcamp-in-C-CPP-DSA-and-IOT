//7. Write a C++ program to calculate the square of a number.
#include<iostream>
using namespace std;
int main()
{
    int x;
    cout<<"Enter a number: ";
    cin>>x;
    cout<<"Square of the given number is: "<<x*x;
    cout<<endl;
    return 0;
}
/*OUTPUT-
Enter a number: 7
Square of the given number is: 49
*/